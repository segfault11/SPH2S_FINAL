#include "ValueTable.h"
#include <cstdio>
//------------------------------------------------------------------------------
ValueTable::ValueTable()
{

}
//------------------------------------------------------------------------------
ValueTable::~ValueTable()
{
    
}
//------------------------------------------------------------------------------
void ValueTable::AddValue(float x, float y)
{
    mXs.push_back(x);
    mYs.push_back(y);
}
//------------------------------------------------------------------------------
void ValueTable::Save(const std::string& filename)
{
    std::FILE* file = std::fopen(filename.c_str(), "w");
    
    for(unsigned int i = 0; i < mXs.size(); i++)
    {
        fprintf(file, "%f %f\n", mXs[i], mYs[i]);
    }
    
    std::fclose(file);
}
